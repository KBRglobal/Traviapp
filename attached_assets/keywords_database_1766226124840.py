def get_all_topics():
    topics = []
    for category_key, category_data in DUBAI_KEYWORDS.items():
        for topic_data in category_data["topics"]:
            topics.append({
                "category": category_key,
                "category_name": category_data["name"],
                "topic": topic_data["topic"],
                "keywords": topic_data["keywords"],
                "priority": topic_data["priority"]
            })
    return sorted(topics, key=lambda x: x["priority"])

def generate_filename(category, topic, image_type, keywords):
    slug = topic.lower().replace(" ", "-").replace("'", "")
    keyword_str = "-".join(keywords[:2]).lower().replace(" ", "-")
    return f"{category}-{slug}-{image_type['suffix']}-{keyword_str}-dubai.jpg"

def generate_search_tags(category, topic, keywords, image_type):
    tags = [category, topic.lower()]
    tags.extend([k.lower() for k in keywords])
    tags.extend(["dubai", "tourism", "travel", image_type["type"]])
    return list(set(tags))
